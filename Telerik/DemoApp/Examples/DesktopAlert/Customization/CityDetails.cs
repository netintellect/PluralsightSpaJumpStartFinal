
namespace Telerik.Windows.Examples.DesktopAlert.Customization
{
    public class CityDetails
    {
        private string country;
        private string cityName;

        public string Country
        {
            get 
            { 
                return country; 
            }
            set 
            { 
                country = value; 
            }
        }

        public string CityName
        {
            get 
            {
                return cityName;
            }
            set 
            { 
                cityName = value; 
            }
        }
    }
}
