namespace Telerik.Windows.Examples.Diagrams.Dashboard
{
	/// <summary>
	/// Enumerates the two type of states in which the application presents itself.
	/// </summary>
	public enum ApplicationState
	{
		/// <summary>
		/// In a state where the user can design the content.
		/// </summary>
		DesignMode,
		/// <summary>
		/// A browse, read-only state of the application.
		/// </summary>
		RunMode
	}
}